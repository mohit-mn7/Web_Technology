function validateEmail(email){
	var recEmail = /^([A-Za-z0-9_\-\.])+\@([A-Za-z0-9_\-\.])+\.([A-Za-z]{2,4})$/;

        if (recEmail.test(email.value) == false) 
        {
            alert('Invalid Email Address');
            return false;
        }
        return true;
}

function validateFname(name){
	var Fname = /^[a-zA-Z ]{2,30}$/;

        if (Fname.test(name.value) == false) 
        {
            alert('Invalid First Name');
            return false;
        }
        return true;
}

